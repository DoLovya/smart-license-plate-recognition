import argparse
import csv
import math
import re
from pathlib import Path

import matplotlib.pyplot as plt


DEFAULT_COLUMNS = [
    "train/box_loss",
    "train/cls_loss",
    "train/dfl_loss",
    "val/box_loss",
    "val/cls_loss",
    "val/dfl_loss",
    "metrics/precision(B)",
    "metrics/recall(B)",
    "metrics/mAP50(B)",
    "metrics/mAP50-95(B)",
]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Plot training curves from an Ultralytics results.csv file."
    )
    parser.add_argument(
        "--csv",
        type=Path,
        required=True,
        help="Path to results.csv generated by training.",
    )
    parser.add_argument(
        "--columns",
        nargs="+",
        default=DEFAULT_COLUMNS,
        help="Columns to plot. Use --list-columns to inspect available names first.",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        help="Directory for generated images. Defaults to a plots folder next to the csv file.",
    )
    parser.add_argument(
        "--combined-name",
        default="combined_metrics.png",
        help="Filename for the combined multi-subplot figure.",
    )
    parser.add_argument(
        "--dpi",
        type=int,
        default=160,
        help="Image DPI.",
    )
    parser.add_argument(
        "--style",
        default="seaborn-v0_8-whitegrid",
        help="Matplotlib style name.",
    )
    parser.add_argument(
        "--list-columns",
        action="store_true",
        help="Print available CSV columns and exit.",
    )
    return parser.parse_args()


def load_rows(csv_path: Path) -> tuple[list[dict[str, str]], list[str]]:
    with csv_path.open("r", encoding="utf-8-sig", newline="") as file:
        reader = csv.DictReader(file)
        rows = list(reader)
        if reader.fieldnames is None:
            raise ValueError(f"CSV header is missing: {csv_path}")
        return rows, reader.fieldnames


def to_float_series(rows: list[dict[str, str]], column: str) -> list[float]:
    series = []
    for row in rows:
        value = row.get(column, "")
        if value in (None, ""):
            series.append(float("nan"))
            continue
        try:
            series.append(float(value))
        except ValueError as exc:
            raise ValueError(f"Column {column} contains a non-numeric value: {value}") from exc
    return series


def sanitize_filename(name: str) -> str:
    return re.sub(r'[^A-Za-z0-9._-]+', "_", name).strip("_") or "plot"


def ensure_output_dir(csv_path: Path, output_dir: Path | None) -> Path:
    target_dir = output_dir or (csv_path.parent / "plots")
    target_dir.mkdir(parents=True, exist_ok=True)
    return target_dir


def plot_single_metric(
    epochs: list[float],
    values: list[float],
    column: str,
    output_path: Path,
    dpi: int,
) -> None:
    figure, axis = plt.subplots(figsize=(8, 4.5))
    axis.plot(epochs, values, marker="o", linewidth=1.8, markersize=3)
    axis.set_title(column)
    axis.set_xlabel("epoch")
    axis.set_ylabel(column)
    axis.grid(True, linestyle="--", alpha=0.35)
    figure.tight_layout()
    figure.savefig(output_path, dpi=dpi)
    plt.close(figure)


def plot_combined_metrics(
    epochs: list[float],
    rows: list[dict[str, str]],
    columns: list[str],
    output_path: Path,
    dpi: int,
) -> None:
    column_count = len(columns)
    grid_cols = 2 if column_count > 1 else 1
    grid_rows = math.ceil(column_count / grid_cols)
    figure, axes = plt.subplots(
        grid_rows,
        grid_cols,
        figsize=(8 * grid_cols, 3.6 * grid_rows),
        squeeze=False,
    )

    for index, column in enumerate(columns):
        axis = axes[index // grid_cols][index % grid_cols]
        values = to_float_series(rows, column)
        axis.plot(epochs, values, marker="o", linewidth=1.6, markersize=2.8)
        axis.set_title(column)
        axis.set_xlabel("epoch")
        axis.set_ylabel(column)
        axis.grid(True, linestyle="--", alpha=0.35)

    for index in range(column_count, grid_rows * grid_cols):
        axes[index // grid_cols][index % grid_cols].axis("off")

    figure.tight_layout()
    figure.savefig(output_path, dpi=dpi)
    plt.close(figure)


def main() -> None:
    args = parse_args()
    rows, fieldnames = load_rows(args.csv)

    if args.list_columns:
        print("Available columns:")
        for column in fieldnames:
            print(f"- {column}")
        return

    if not rows:
        raise ValueError(f"CSV has no data rows: {args.csv}")

    missing_columns = [column for column in args.columns if column not in fieldnames]
    if missing_columns:
        raise ValueError(
            "Unknown columns: "
            + ", ".join(missing_columns)
            + ". Use --list-columns to inspect the CSV schema."
        )

    plt.style.use(args.style)

    output_dir = ensure_output_dir(args.csv, args.output_dir)
    epochs = to_float_series(rows, "epoch")

    for column in args.columns:
        output_path = output_dir / f"{sanitize_filename(column)}.png"
        values = to_float_series(rows, column)
        plot_single_metric(epochs, values, column, output_path, dpi=args.dpi)
        print(f"Saved {output_path}")

    combined_path = output_dir / args.combined_name
    plot_combined_metrics(epochs, rows, args.columns, combined_path, dpi=args.dpi)
    print(f"Saved {combined_path}")


if __name__ == "__main__":
    main()
